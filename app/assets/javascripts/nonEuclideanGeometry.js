// \section * { Euclidean Geometry }
// Euclidean Geometry is based an earlier knowledge of geometry dating back 300BC.Defined by the five Euclid(Named after Mathematician Alexandrian Euclid)Axioms or Common Notions.
// \newline
// 1. A straight line may be drawn from any given point to any other
// \newline
// 2. A straight line may be extended to any finite length
// \newline
// 3. A circle may be described with any given point as its center and any distance as its radius.
// \newline
// 4. All right angles are congruent
// \newline
// 5. If a straight line intersects two other straight lines, and so makes the two interior angles on one side of it together less than the two right angles, then the other straight lines will meet at a point if extended far enough on the side on which the angles are less than two right angles.
// \newline
// \newline
// The distance between two points is formally defined as:
// \[
//   d, (P, Q) = \sqrt{(p_1 - q_1) ^ 2 + (p_2 - q_2) ^ 2}
//   \]
// \newline
// \newline
// \subsection * { Non Euclidean Geometry }
// Non - euclidean geometry arises when either the metric requirement is relaxed, or when the fifth axiom as noted above, also known as the parallel postulate is replaced with an alternative one.In the latter one obtains hyperbolic and elliptic geometry.The traditional Non - Euclidean Geometry.

